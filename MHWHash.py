	for type in fileTypes:
		extension = type[0]
		function = type[1]
		hash = (zlib.crc32(type[1].encode()) ^ 0xffffffff) & 0x7fffffff
		hashHex = hex(hash)
		print(f"{extension},{function},{hash},{hashHex}")
	
